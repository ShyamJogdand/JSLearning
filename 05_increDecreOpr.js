// index ++  ==> index = index + 1
// index --  ==> index = index -1

console.log("Increment operator: +1");
var index = 10;
var y = index+1;
console.log(`Index: ${index}, y: ${y}`);

console.log("Increment operator: ++");
var index = 10;
var y = index++;
console.log(`Index: ${index}, y: ${y}`);

console.log("Decrement operator: -1");
var index = 10;
var y = index -1;
console.log(`Index: ${index}, y: ${y}`);

console.log("Decrement operator: --");
var index = 10;
var y = index --;
console.log(`Index: ${index}, y: ${y}`);

// ++index  ==> index = +1 index
// --index  ==> index = -1index

console.log("Pre increment operator");
var index = 10;
var y = ++index;
console.log(`Index: ${index}, y: ${y}`);

console.log("Pre decrement operator");
var index = 10;
var y = --index;
console.log(`Index: ${index}, y: ${y}`);