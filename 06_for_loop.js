// Print number from 1 to 10
// initialization 1
// update expression +1
// condition <=10

console.log("Pirng numbers 1 to 10");
for (let index = 1; index <=10 ; index=index+1) {
    console.log(index);
}

console.log("Pirng numbers 0 to 5");
for (let index = 0; index <=5; index=index +1) {
    console.log(index);    
}

console.log("Reverse numbe");
for (let index = 10; index >= 0; index=index-1) {
    console.log(index);
}

console.log("table of 5");
for (let index = 5; index <=50 ; index=index +5) {
    console.log(index);    
}

